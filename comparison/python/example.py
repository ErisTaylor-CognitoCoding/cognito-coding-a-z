# Comparison operators — asking "is this bigger / equal / smaller?"

a = 10
b = 20

print(a > b)    # False  — is a greater than b?
print(a < b)    # True   — is a less than b?
print(a == b)   # False  — are they equal? (== not =)
print(a != b)   # True   — are they NOT equal?
print(a >= 10)  # True   — greater than OR equal to?
print(b <= 20)  # True   — less than OR equal to?
